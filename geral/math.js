const num1 = 9.5467
let num2 = Math.floor(num1) // arredondar para baixo
let num3 = Math.ceil(num1) // arredondar para cima
let num4 = Math.round(num1) // arredonda pelo 0.5

console.log(num1 + ' = ' + num2 + ' arredondado com floor');
console.log(num1, ' = ',num3, ' arredondado com ceil');
console.log(num1, ' = ',num4, ' arredondado com round')